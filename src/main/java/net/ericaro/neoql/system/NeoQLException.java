package net.ericaro.neoql.system;

public class NeoQLException extends RuntimeException {

	public NeoQLException() {
		super();
	}


	public NeoQLException(String message, Throwable cause) {
		super(message, cause);
	}

	public NeoQLException(String message) {
		super(message);
	}

	public NeoQLException(Throwable cause) {
		super(cause);
	}

	
	
}
